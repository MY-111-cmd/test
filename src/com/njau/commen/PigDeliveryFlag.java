package com.njau.commen;

public class PigDeliveryFlag {
	private int id;
	private String num;
	private int isalarm;
	private int flag;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public int getIsalarm() {
		return isalarm;
	}

	public void setIsalarm(int isalarm) {
		this.isalarm = isalarm;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

}
